package com.mutong.offer;

/**
 * @description:
 * @Author: Mutong
 * @Date: 2020-03-26 11:33
 * @time_complexity: O()
 */
public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
